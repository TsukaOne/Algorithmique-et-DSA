def count(arr, n, x):
    def first_occurrence(arr, n, x):
        low, high = 0, n - 1
        while low <= high:
            mid = (low + high) // 2
            if arr[mid] == x and (mid == 0 or arr[mid - 1] < x):
                return mid
            elif arr[mid] < x:
                low = mid + 1
            else:
                high = mid - 1
        return -1

